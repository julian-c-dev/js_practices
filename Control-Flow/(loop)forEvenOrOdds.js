// Loop For

for (i = 0; i < 5; i++) {
  console.log("hi mate", i);
}

for (i = 1; i <= 5; i++) {
  console.log("hi pal", i);
}

/*Getting odd numbers*/

for (i = 1; i <= 10; i++) {
  if (i % 2 !== 0) console.log(i);
}

/*Getting even numbers*/

for (i = 1; i <= 10; i++) {
  if (i % 2 === 0) console.log(i);
}
